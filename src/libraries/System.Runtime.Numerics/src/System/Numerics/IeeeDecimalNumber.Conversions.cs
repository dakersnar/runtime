        {
            uint i = ((uint)s << 31) + ((uint)e << 23) + (uint)c;
            return BitConverter.UInt32BitsToSingle(i);
        }

        // Special cases of returning zero, infinity, NaN as binary FP
        // Take parameters for the sign, and for NaN the significand
        private static float return_binary32_zero(int s) => return_binary32(s, 0, 0);
        private static float return_binary32_inf(int s) => return_binary32(s, 255, 0);
        private static float return_binary32_nan(int s, ulong c_hi) => return_binary32(s, 255, (c_hi >> 42) + (1u << 22)); // TODO are these parameter types right?


        // Unpack binary floating-point number x into
        //
        // int    s (sign in the LSB)
        // int    e (true "integer" exponent)
        //        c (normalized coefficient with explicit 1 bit)
        //        t (trailing zero count, valid in normalized case only)
        //        [c_hi,c_lo in the case of quad]
        //
        // Return given Zero, Infinity or NaN Classifications if appropriate

        private static Classification unpack_binary32(uint x, out int s, out int e, out ulong c, out int t) // TODO: confirm that c should be a ulong here
        {
            // PORT: remove status flag setting in this function

            c = x;
            e = (int)((c >> 23) & ((1UL << 8) - 1)); // PORT: add this cast to int
            s = (int)c >> 31; // PORT: add this cast to int
            c &= ((1UL << 23) - 1);
            if (e == 0)
            {
                int l;
                if (c == 0)
                {
                    // return default values to satisfy "out", these will go unused
                    t = 0;
                    return Classification.Zero;
                }
                l = clz32(c) - (32 - 24);
                c <<= l;
                e = -(l + 149);
                t = 0;
            }
            else if (e == (int)((1UL << 8) - 1)) // PORT: add this cast to int
            {
                if (c == 0)
                {
                    // return default values to satisfy "out", these will go unused
                    t = 0;
                    return Classification.Infinity;
                }
                else
                {
                    // return default values to satisfy "out", these will go unused
                    t = 0;
                    return Classification.NaN;
                }
            }
            else
            {
                c += 1UL << 23;
                t = ctz32((long)c);
                e -= 150;
            }
            return Classification.Finite;
        }


        // Pack and return decimal number from raw fields
        private static Decimal32 return_bid32(int s, int e, ulong c) // TODO this is probably duplication of logic in Decimal32.cs. Do we want to combine them or keep this separate for the sake of minimizing changes to the ported code?
        {
            if ((uint)(c) < (1ul << 23))
            {
                return new Decimal32(((uint)s << 31) + ((uint)e << 23) + (uint)c);
            }
            else
            {
                return new Decimal32(((uint)s << 31) + (uint)((0x3UL << 29) - (1UL << 23)) + ((uint)e << 21) + (uint)c); // PORT: add uint cast to ((0x3UL << 29) - (1UL << 23))
            }
        }

        // Special cases of returning zero, infinity, NaN as binary FP
        // Take parameters for the sign, and for NaN the significand
        private static Decimal32 return_bid32_zero(int s) => return_bid32(s, 101, 0);
        private static Decimal32 return_bid32_inf(int s) => return_bid32(s, (0xF << 4), 0);
        private static Decimal32 return_bid32_nan(int s, ulong c_hi) => return_bid32(s, (0x1F << 3), (((c_hi >> 44) > 999999u) ? 0 : (c_hi >> 44))); // TODO are the types of these parameters right?



        // Unpack decimal floating-point number x into sign,exponent,coefficient
        // In special cases, return the appropriate Classifcation to be handled by the caller
        // Coefficient is normalized in the binary sense with postcorrection k,
        // so that x = 10^e * c / 2^k and the range of c is:
        //
        // 2^23 <= c < 2^24   (decimal32)
        // 2^53 <= c < 2^54   (decimal64)
        // 2^112 <= c < 2^113 (decimal128)

        private static Classification unpack_bid32(uint x, out int s, out int e, out int k, out ulong c)
        {
            s = (int)x >> 31;
            if ((x & (3UL << 29)) == (3UL << 29))
            {
                if ((x & (0xFUL << 27)) == (0xFUL << 27))
                {
                    // return default values to satisfy "out", these will go unused
                    e = 0;
                    k = 0;
                    c = 0;

                    if ((x & (0x1FUL << 26)) != (0x1FUL << 26))
                    {
                        return Classification.Infinity;
                    }

                    // if ((x & (1ul<<25))!=0) __set_status_flags(pfpsf, BID_INVALID_EXCEPTION);
                    return Classification.NaN;

                }

                e = (int)((x >> 21) & ((1UL << 8) - 1)) - 101;
                c = (1UL << 23) + (x & ((1UL << 21) - 1));
                if ((uint)(c) > 9999999u)
                {
                    k = 0; // return default value to satisfy "out", this will go unused
                    return Classification.Zero;
                }
                k = 0;
            }
            else
            {
                e = (int)((x >> 23) & ((1UL << 8) - 1)) - 101;
                c = x & ((1UL << 23) - 1);
                if (c == 0)
                {
                    k = 0; // return default value to satisfy "out", this will go unused
                    return Classification.Zero;
                }
                k = clz32_nz(c) - 8;
                c <<= k;
            }

            return Classification.Finite;
        }

        public static void __add_carry_out(out ulong S, out ulong CY, ulong X, ulong Y)
        {
            ulong X1 = X;
            S = X + Y;
            CY = (S < X1) ? 1UL : 0UL;
        }
        public static void __add_carry_in_out(out ulong S, ref ulong CY, ulong X, ulong Y, ulong CI)
        {
            ulong X1;
            X1 = X + CI;
            S = X1 + Y;
            CY = ((S < X1) || (X1 < CI)) ? 1UL : 0UL;
        }


        // get full 64x64bit product
        //
        private static void __mul_64x64_to_128(out (ulong, ulong) P, ulong CX, ulong CY)
        {
            ulong CXH, CXL, CYH, CYL, PL, PH, PM, PM2;
            CXH = (CX) >> 32;
            CXL = (uint)(CX);
            CYH = (CY) >> 32;
            CYL = (uint)(CY);

            PM = CXH * CYL;
            PH = CXH * CYH;
            PL = CXL * CYL;
            PM2 = CXL * CYH;
            PH += (PM >> 32);
            PM = (ulong)((uint)PM) + PM2 + (PL >> 32);

            P.Item2 = PH + (PM >> 32);
            P.Item1 = (PM << 32) + (uint)PL;
        }
        private static void __mul_128x128_low(out (ulong, ulong) Ql, (ulong, ulong) A, (ulong, ulong) B)
        {
            (ulong, ulong) ALBL;
            ulong QM64;

            __mul_64x64_to_128(out ALBL, A.Item1, B.Item1);
            QM64 = B.Item1 * A.Item2 + A.Item1 * B.Item2;

            Ql.Item1 = ALBL.Item1;
            Ql.Item2 = QM64 + ALBL.Item2;
        }

        private static void __mul_64x256_to_320(out (ulong, ulong, ulong, ulong, ulong, ulong) P, ulong A, (ulong, ulong, ulong, ulong) B)
        {
            (ulong, ulong) lP0;
            (ulong, ulong) lP1;
            (ulong, ulong) lP2;
            (ulong, ulong) lP3;
            ulong lC;

            __mul_64x64_to_128(out lP0, A, B.Item1);
            __mul_64x64_to_128(out lP1, A, B.Item2);
            __mul_64x64_to_128(out lP2, A, B.Item3);
            __mul_64x64_to_128(out lP3, A, B.Item4);
            P.Item1 = lP0.Item1;
            __add_carry_out(out P.Item2, out lC, lP1.Item1, lP0.Item2);
            __add_carry_in_out(out P.Item3, ref lC, lP2.Item1, lP1.Item2, lC);
            __add_carry_in_out(out P.Item4, ref lC, lP3.Item1, lP2.Item2, lC);
            P.Item5 = lP3.Item2 + lC;
            P.Item6 = 0; // this will get overridden or ignored
        }

        // 128x256->384 bit multiplication (missing from existing macros)
        // This was derived by propagating A.Item3 = 0 in __mul_192x256_to_448
        public static void __mul_128x256_to_384(out (ulong, ulong, ulong, ulong, ulong, ulong) P, (ulong, ulong) A, (ulong, ulong, ulong, ulong) B)
        {
            (ulong, ulong, ulong, ulong, ulong, ulong) P0, P1; // PORT: the original code has this as 512 bits (8 ulongs), but we only need 6
            ulong CY;
            __mul_64x256_to_320(out P0, A.Item1, B);
            __mul_64x256_to_320(out P1, A.Item2, B);
            P.Item1 = P0.Item1;
            __add_carry_out(out P.Item2, out CY, P1.Item1, P0.Item2);
            __add_carry_in_out(out P.Item3, ref CY, P1.Item2, P0.Item3, CY);
            __add_carry_in_out(out P.Item4, ref CY, P1.Item3, P0.Item4, CY);
            __add_carry_in_out(out P.Item5, ref CY, P1.Item4, P0.Item5, CY);
            P.Item6 = P1.Item5 + CY;
        }

        public static float Decimal32ToSingle(Decimal32 value)
        {
            uint x = value._value;

            ulong c_prov;
            (ulong, ulong) c; // tuple containing 2 ulongs for 128 bits total
            (ulong, ulong) m_min; // tuple containing 2 ulongs for 128 bits total
            int s, e, k, e_out;
            (ulong, ulong, ulong, ulong) r; // tuple containing 4 ulongs for 256 bits total
            (ulong, ulong, ulong, ulong, ulong, ulong) z; // tuple containing 6 ulongs for 384 bits total

            // PORT: Removed support for alternative rounding modes

            // Unpack the input
            switch (unpack_bid32(x, out s, out e, out k, out c.Item2))
            {
                case Classification.Infinity: return return_binary32_inf(s);
                case Classification.Zero: return return_binary32_zero(s);
                case Classification.NaN: return return_binary32_nan(s, ((x & 0xFFFFFu) > 999999u) ? 0UL : (((ulong)x) << 44));
                    // Classification.Finite will continue onward
            }

            // Correct to 2^112 <= c < 2^113 with corresponding exponent adding 113-24=89
            // Thus a shift of 25 given that we've already upacked in c[1]

            c.Item2 <<= 25;
            c.Item1 = 0;
            k += 89;

            // Check for "trivial" overflow, when 10^e * 1 > 2^{sci_emax+1}, just to
            // keep tables smaller (it would be intercepted later otherwise).
            //
            // (Note that we may have normalized the coefficient, but we have a
            //  corresponding exponent postcorrection to account for; this can
            //  afford to be conservative anyway.)
            //
            // We actually check if e >= ceil((sci_emax + 1) * log_10(2))
            // which in this case is e >= ceil(128 * log_10(2)) = 39

            if (e >= 39)
            {
                // PORT: Removed support for status flag setting
                return return_binary32_inf(s);
            }
            // Also check for "trivial" underflow, when 10^e * 2^113 <= 2^emin * 1/4,
            // so test e <= floor((emin - 115) * log_10(2))
            // In this case just fix ourselves at that value for uniformity.
            //
            // This is important not only to keep the tables small but to maintain the
            // testing of the round/sticky words as a correct rounding method

            if (e <= -80)
            {
                e = -80;
            }

            // Look up the breakpoint and approximate exponent

            m_min = (bid_breakpoints_binary32[2 * (e + 80)], bid_breakpoints_binary32[2 * (e + 80) + 1]);
            e_out = bid_exponents_binary32[e + 80] - k;

            // Choose provisional exponent and reciprocal multiplier based on breakpoint
            if (c.Item2 <= m_min.Item2)
            {
                r = (bid_multipliers1_binary32[4 * (e + 80)],
                    bid_multipliers1_binary32[4 * (e + 80) + 1],
                    bid_multipliers1_binary32[4 * (e + 80) + 2],
                    bid_multipliers1_binary32[4 * (e + 80) + 3]);
            }
            else
            {
                r = (bid_multipliers2_binary32[4 * (e + 80)],
                    bid_multipliers2_binary32[4 * (e + 80) + 1],
                    bid_multipliers2_binary32[4 * (e + 80) + 2],
                    bid_multipliers2_binary32[4 * (e + 80) + 3]);
                e_out++;
            }

            // Do the reciprocal multiplication
            __mul_64x256_to_320(out z, c.Item2, r);
            z.Item6 = z.Item5; z.Item5 = z.Item4; z.Item4 = z.Item3; z.Item3 = z.Item2; z.Item2 = z.Item1; z.Item1 = 0;

            // Check for exponent underflow and compensate by shifting the product
            // Cut off the process at precision+2, since we can't really shift further
            if (e_out < 1)
            {
                int d;
                d = 1 - e_out;
                if (d > 26)
                    d = 26;
                e_out = 1;
                srl256_short(ref z.Item6, ref z.Item5, ref z.Item4, ref z.Item3, d);
            }
            c_prov = z.Item6;

            // Round using round-sticky words
            // If we spill into the next binade, correct
            // Flag underflow where it may be needed even for |result| = SNN

            if (lt128(
                bid_roundbound_128[((s & 1) << 1) + (int)(c_prov & 1)].Item2, // PORT: the calculation of this index factors in the rounding mode in the original code
                bid_roundbound_128[((s & 1) << 1) + (int)(c_prov & 1)].Item1,
                z.Item5,
                z.Item4
                ))
            {
                c_prov++;
                if (c_prov == (1UL << 24))
                {
                    c_prov = 1UL << 23;
                    e_out++;
                }
            }

            // Check for overflow
            if (e_out >= 255)
            {
                // PORT: Removed support for status flag setting

                // PORT: the original implementation calls return_binary32_ovf,
                // which sometimes returns inf sometimes returns max based on rounding mode.
                // We do not support alternate rounding modes, so this will always return inf
                return return_binary32_inf(s);
            }

            // Modify exponent for a tiny result, otherwise lop the implicit bit
            if (c_prov < (1UL << 23))
            {
                e_out = 0;
            }
            else
            {
                c_prov &= ((1UL << 23) - 1);
            }

            // PORT: Removed support for status flag setting

            // Package up the result as a binary floating-point number
            return return_binary32(s, e_out, c_prov);
        }

        public static Decimal32 SingleToDecimal32(float value)
        {
            uint x = BitConverter.SingleToUInt32Bits(value);

            (ulong, ulong) c; // BID_UINT128
            ulong c_prov; // BID_UINT64
            (ulong, ulong) m_min; // BID_UINT128
            (ulong, ulong, ulong, ulong) r; // BID_UINT256
            (ulong, ulong, ulong, ulong, ulong, ulong) z; // BID_UINT384

            int e, s, t, e_out;

            // PORT: Removed support for alternative rounding modes

            // Unpack the input
            switch (unpack_binary32(x, out s, out e, out c.Item2, out t))
            {
                case Classification.Infinity: return return_bid32_inf(s);
                case Classification.Zero: return return_bid32_zero(s);
                case Classification.NaN: return return_bid32_nan(s, c.Item2 << 42);
                    // Classification.Finite will continue onward
            }

            // Now -172<=e<=104 (104 for max normal, -149 for min normal, -172 for min denormal)

            // Treat like a quad input for uniformity, so (2^{113-24} * c * r) >> 320,
            // where 320 is the truncation value for the reciprocal multiples, exactly
            // five 64-bit words. So we shift 113-24=89 places. Since we unpacked in
            // the high end, shift a further 89-64=25 places
            //
            // Remember to compensate for the fact that exponents are integer for quad

            c.Item1 = 0;
            c.Item2 <<= 25;
            t += (113 - 24);
            e -= (113 - 24); // Now e belongs [-238..15]

            // Check for "trivial" overflow, when 2^e * 2^112 > 10^emax * 10^d.
            // We actually check if e >= ceil((emax + d) * log_2(10) - 112)
            // This could be intercepted later, but it's convenient to keep tables smaller

            // Now filter out all the exact cases where we need to specially force
            // the exponent to 0. We can let through inexact cases and those where the
            // main path will do the right thing anyway, e.g. integers outside coeff range.
            //
            // First check that e <= 0, because if e > 0, the input must be >= 2^113,
            // which is too large for the coefficient of any target decimal format.
            // We write a = -(e + t)
            //
            // (1) If e + t >= 0 <=> a <= 0 the input is an integer; treat it specially
            //     iff it fits in the coefficient range. Shift c' = c >> -e, and
            //     compare with the coefficient range; if it's in range then c' is
            //     our coefficient, exponent is 0. Otherwise we pass through.
            //
            // (2) If a > 0 then we have a non-integer input. The special case would
            //     arise as c' / 2^a where c' = c >> t, i.e. 10^-a * (5^a c'). Now
            //     if a > 48 we can immediately forget this, since 5^49 > 10^34.
            //     Otherwise we determine whether we're in range by a table based on
            //     a, and if so get the multiplier also from a table based on a.

            if (e <= 0)
            {
                (ulong, ulong) cint;
                int a = -(e + t);
                cint.Item2 = c.Item2; cint.Item1 = c.Item1;
                if (a <= 0)
                {
                    srl128(ref cint.Item2, ref cint.Item1, -e);
                    if ((cint.Item2 == 0) && (cint.Item1 < 10000000UL))
                    {
                        return return_bid32(s, 101, cint.Item1);
                    }
                }
                else if (a <= 48)
                {
                    (ulong, ulong) pow5 = (bid_coefflimits_bid32[2 * a], bid_coefflimits_bid32[2 * a + 1]);
                    srl128(ref cint.Item2, ref cint.Item1, t);
                    if (le128(cint.Item2, cint.Item1, pow5.Item2, pow5.Item1))
                    {
                        (ulong, ulong) cc;
                        cc.Item2 = cint.Item2;
                        cc.Item1 = cint.Item1;
                        pow5 = (bid_power_five[a], bid_power_five[a + 1]);
                        __mul_128x128_low(out cc, cc, pow5); // TODO: should this helper take in a "ref" instead of an "out"?
                        return return_bid32(s, 101 - a, cc.Item1);
                    }
                }
            }
            // Check for "trivial" underflow, when 2^e * 2^113 <= 10^emin * 1/4,
            // so test e <= floor(emin * log_2(10) - 115)
            // In this case just fix ourselves at that value for uniformity.
            //
            // This is important not only to keep the tables small but to maintain the
            // testing of the round/sticky words as a correct rounding method

            // Now look up our exponent e, and the breakpoint between e and e+1

            m_min = (bid_breakpoints_bid32[2 * (e + 450)], bid_breakpoints_bid32[2 * (e + 450) + 1]);
            e_out = bid_exponents_bid32[e + 450];

            // Choose exponent and reciprocal multiplier based on breakpoint

            if (le128(c.Item2, c.Item1, m_min.Item2, m_min.Item1))
            {
                r = (bid_multipliers1_bid32[4 * (e + 450)],
                    bid_multipliers1_bid32[4 * (e + 450) + 1],
                    bid_multipliers1_bid32[4 * (e + 450) + 2],
                    bid_multipliers1_bid32[4 * (e + 450) + 3]);
            }
            else
            {
                r = (bid_multipliers2_bid32[4 * (e + 450)],
                    bid_multipliers2_bid32[4 * (e + 450) + 1],
                    bid_multipliers2_bid32[4 * (e + 450) + 2],
                    bid_multipliers2_bid32[4 * (e + 450) + 3]);
                e_out++;
            }

            // Do the reciprocal multiplication

            __mul_128x256_to_384(out z, c, r);
            c_prov = z.Item6;

            // Round using round-sticky words
            // If we spill over into the next decade, correct
            if (lt128(
                bid_roundbound_128[((s & 1) << 1) + (int)(c_prov & 1)].Item2, // PORT: the calculation of this index factors in the rounding mode in the original code
                bid_roundbound_128[((s & 1) << 1) + (int)(c_prov & 1)].Item1,
                z.Item5,
                z.Item4
                ))
            {
                c_prov++;
                if (c_prov == 10000000UL)
                {
                    c_prov = 1000000UL;
                    e_out++;
                }
            }

            // PORT: Remove status flag setting for overflow

            // Package up the result

            return return_bid32(s, e_out, c_prov);
        }
