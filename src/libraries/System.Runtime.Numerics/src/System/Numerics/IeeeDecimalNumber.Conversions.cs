        // Counting leading zeros in an unsigned 32-bit word
        // The "_nz" version will return the wrong answer (31) for zero inputs

        private const uint CLZ32_MASK16 = 0xFFFF0000u;
        private const uint CLZ32_MASK8 = 0xFF00FF00u;
        private const uint CLZ32_MASK4 = 0xF0F0F0F0u;
        private const uint CLZ32_MASK2 = 0xCCCCCCCCu;
        private const uint CLZ32_MASK1 = 0xAAAAAAAAu;

        private static int clz32_nz(ulong n)
        {
            return (((n & CLZ32_MASK16) <= (n & ~CLZ32_MASK16)) ? 16 : 0)
                    + (((n & CLZ32_MASK8) <= (n & ~CLZ32_MASK8)) ? 8 : 0)
                    + (((n & CLZ32_MASK4) <= (n & ~CLZ32_MASK4)) ? 4 : 0)
                    + (((n & CLZ32_MASK2) <= (n & ~CLZ32_MASK2)) ? 2 : 0)
                    + (((n & CLZ32_MASK1) <= (n & ~CLZ32_MASK1)) ? 1 : 0);
        }

        private static int clz32(ulong n) => (n == 0) ? 32 : clz32_nz(n);

        // Counting trailing zeros in an unsigned 32-bit word
        // The ctz32_1bit version is for a single bit

        private static int ctz32_1bit(long n) // PORT: changed argument from "ulong" to "long"
        {
            return (((n & ~CLZ32_MASK16) != 0) ? 0 : 16) // PORT: add the "!= 0" check that I think is implied in C
                + (((n & ~CLZ32_MASK8) != 0) ? 0 : 8)
                + (((n & ~CLZ32_MASK4) != 0) ? 0 : 4)
                + (((n & ~CLZ32_MASK2) != 0) ? 0 : 2)
                + (((n & ~CLZ32_MASK1) != 0) ? 0 : 1);
        }

        private static int ctz32(long n) => (n == 0) ? 32 : ctz32_1bit(n & -n); // PORT: changed argument from "ulong" to "long"

        // Shift 2-part 2^64 * hi + lo left by "c" bits
        // The "short" form requires a shift 0 < c < 64 and will be faster
        // Note that shifts of 64 can't be relied on as ANSI

        private static void sll128_short(ref ulong hi, ref ulong lo, int c)
        {
            hi = (hi << c) + (lo >> (64 - c));
            lo <<= c;
        }

        private static void sll128(ref ulong hi, ref ulong lo, int c)
        {
            if (c == 0)
            {
                return;
            }
            else if (c >= 64)
            {
                hi = lo << (c - 64);
                lo = 0;
            }
            else
            {
                srl128_short(ref hi, ref lo, c);
            }
        }

        // Shift 2-part 2^64 * hi + lo right by "c" bits
        // The "short" form requires a shift 0 < c < 64 and will be faster
        // Note that shifts of 64 can't be relied on as ANSI

        private static void srl128_short(ref ulong hi, ref ulong lo, int c)
        {
            lo = (hi << (64 - c)) + (lo >> c);
            hi >>= c;
        }

        private static void srl128(ref ulong hi, ref ulong lo, int c)
        {
            if (c == 0)
            {
                return;
            }
            else if (c >= 64)
            {
                lo = hi >> (c - 64);
                hi = 0;
            }
            else
            {
                srl128_short(ref hi, ref lo, c);
            }

        }

        // Shift 4-part 2^196 * x3 + 2^128 * x2 + 2^64 * x1 + x0
        // right by "c" bits (must have c < 64)
        private static void srl256_short(ref ulong x3, ref ulong x2, ref ulong x1, ref ulong x0, int c)
        {
            (x0) = ((x1) << (64 - c)) + ((x0) >> c);
            (x1) = ((x2) << (64 - c)) + ((x1) >> c);
            (x2) = ((x3) << (64 - c)) + ((x2) >> c);
            (x3) >>= c;
        }

        // Compare "<" two 2-part unsigned integers
        private static bool lt128(ulong x_hi, ulong x_lo, ulong y_hi, ulong y_lo)
        {
            return (((x_hi) < (y_hi)) || (((x_hi) == (y_hi)) && ((x_lo) < (y_lo))));
        }

